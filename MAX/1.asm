;只能求 三个数中最大的值(一位数),负数还尚未涉猎
DATAS SEGMENT
	X DB ?
	Y DB ?
	Z DB ?
	STR1 DB "Input three numbers: ","$"
	STR2 DB "The largest number is: ","$"
	STR3 DB "Have to end?(y/n)","$"
DATAS ENDS

STACKS SEGMENT

STACKS ENDS

CODES SEGMENT
    ASSUME CS:CODES,DS:DATAS,SS:STACKS
START:
    MOV AX,DATAS
    MOV DS,AX
LP:
	MOV AH,2
	MOV DL,10
	INT 21H
	MOV AH,9
	LEA DX,STR1
	INT 21H
	
	MOV AH,1
	INT 21H
	MOV X,AL
	MOV AH,2
	MOV DL,20H
	INT 21H
	
	MOV AH,1
	INT 21H
	MOV AH,2
	MOV Y,AL
	MOV DL,20H
	INT 21H
	
	MOV AH,1
	INT 21H
	MOV Z,AL
	MOV AH,2
	MOV DL,20H
	INT 21H
	
	MOV AH,2
	MOV DL,10
	INT 21H
	MOV AH,9
	LEA DX,STR2
	INT 21H
	MOV AL,Y
	CMP X,AL
	JGE LP1
	MOV X,AL
LP1:
	MOV AL,Z
	CMP X,AL
	JGE EXIT
	MOV X,AL
EXIT:
	MOV AH,2
	MOV DL,X
	INT 21H
	
	MOV AH,2
	MOV DL,10
	INT 21H	
	MOV AH,9
	MOV DX,OFFSET STR3
	INT 21H
	MOV AH,1
	INT 21H
	CMP AL,"n"
	JE LP

    MOV AH,4CH
    INT 21H
CODES ENDS
    END START

