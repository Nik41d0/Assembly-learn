;思路:W=X+Y-Z的灵活应用、IDIV、IMUL
DATAS SEGMENT  
    X DB ?
    SHANG DB ?
    YU DB ?
    SHANG_1 DB ?
    YU_1 DB ?
    STR1 DB "The remainder is:","$"
DATAS ENDS

STACKS SEGMENT

STACKS ENDS

CODES SEGMENT
    ASSUME CS:CODES,DS:DATAS,SS:STACKS
START:
    MOV AX,DATAS
    MOV DS,AX
	
	MOV AH,2
	MOV DL,"("
	INT 21H
	MOV AH,2
	MOV DL,"("
	INT 21H
	
	MOV AH,1
	INT 21H
	MOV X,AL 
	
	MOV AH,2
	MOV DL,"+"
	INT 21H
	
	MOV AH,1
	INT 21H
	ADD X,AL
	SUB X,60H
	MOV AL,X
	
	MOV AH,2
	MOV DL,")"
	INT 21H
	MOV AH,2
	MOV DL,"*"
	INT 21H
	MOV AH,2
	MOV DL,"2"
	INT 21H
	
	MOV AX,0
	MOV AL,X
	MOV BL,2
	IMUL BL
	MOV X,AL
	
	MOV AH,2
	MOV DL,"-"
	INT 21H
	
	MOV AH,1
	INT 21H  
	ADD X,30H 
	SUB X,AL 
	
	MOV AH,2
	MOV DL,")"
	INT 21H
	MOV AH,2
	MOV DL,"/"
	INT 21H
	MOV AH,2
	MOV DL,"2"
	INT 21H
	
	MOV AX,0
	MOV AL,X
    MOV BL,2
	IDIV BL
	MOV SHANG,AL
	MOV YU,AH
	
	MOV AH,2
	MOV DL,"="
	INT 21H

	MOV AX,0
	MOV AL,SHANG
	MOV BL,10	
	DIV BL
	MOV SHANG_1,AL
	MOV YU_1,AH
	
	MOV AH,2
	MOV DL,SHANG_1
	ADD DL,30H
	INT 21H
	MOV AH,2
	MOV DL,YU_1
	ADD DL,30H
	INT 21H
		
	MOV AH,2
	MOV DL,0AH
	INT 21H
	LEA DX,STR1
	MOV AH,9
	INT 21H
	
	MOV AH,2
	ADD YU,30H
	MOV DL,YU
	INT 21H

    MOV AH,4CH
    INT 21H
